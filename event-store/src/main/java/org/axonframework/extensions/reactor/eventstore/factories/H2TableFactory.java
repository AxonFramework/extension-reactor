package org.axonframework.extensions.reactor.eventstore.factories;


/**
 * Jdbc table factory for HSQL databases.
 *
 * @author Rene de Waele
 * @since 3.0
 */
public class H2TableFactory extends AbstractEventTableFactory {

    /**
     * Singleton H2TableFactory instance
     */
    public static final H2TableFactory INSTANCE = new H2TableFactory();

    /**
     * Default constructor of the factory
     */
    protected H2TableFactory() {
    }

    @Override
    protected String idColumnType() {
        return "BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 0)";
    }

    @Override
    protected String payloadType() {
        return "bytea";
    }
}
